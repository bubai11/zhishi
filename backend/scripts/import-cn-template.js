const fs = require('fs');
const path = require('path');
const mysql = require('mysql2/promise');
const sequelizeConfig = require('../config/config').development;

const dbConfig = {
    host: sequelizeConfig.host,
    user: sequelizeConfig.username,
    password: sequelizeConfig.password,
    database: process.env.WCVP_DB_NAME || sequelizeConfig.database
};

const INPUT_CSV = process.env.CN_IMPORT_FILE || path.join(__dirname, '..', 'data', 'cn-pending-template.csv');
const REPORT_DIR = path.join(__dirname, '..');

function parseCsvLine(line) {
    const out = [];
    let cur = '';
    let inQuote = false;

    for (let i = 0; i < line.length; i += 1) {
        const ch = line[i];
        if (ch === '"') {
            if (inQuote && line[i + 1] === '"') {
                cur += '"';
                i += 1;
            } else {
                inQuote = !inQuote;
            }
        } else if (ch === ',' && !inQuote) {
            out.push(cur);
            cur = '';
        } else {
            cur += ch;
        }
    }
    out.push(cur);
    return out;
}

function hasChinese(text) {
    return /[\u3400-\u9FFF]/.test(String(text || ''));
}

function shanghaiDateParts(date = new Date()) {
    const formatter = new Intl.DateTimeFormat('en-CA', {
        timeZone: 'Asia/Shanghai',
        year: 'numeric',
        month: '2-digit',
        day: '2-digit',
        hour: '2-digit',
        minute: '2-digit',
        second: '2-digit',
        hour12: false
    });

    const parts = Object.fromEntries(
        formatter.formatToParts(date).map((part) => [part.type, part.value])
    );

    return {
        day: `${parts.year}-${parts.month}-${parts.day}`,
        timestamp: `${parts.year}-${parts.month}-${parts.day} ${parts.hour}:${parts.minute}:${parts.second}`
    };
}

function appendImportReport(summary) {
    const { day, timestamp } = shanghaiDateParts();
    const reportPath = path.join(REPORT_DIR, `HIGH_PRIORITY_MANUAL_PIPELINE_${day}.md`);
    const lines = [];

    if (!fs.existsSync(reportPath)) {
        lines.push('# High Priority Manual Completion Pipeline');
        lines.push('');
        lines.push(`Date: ${day}`);
        lines.push('');
        lines.push('This file is generated by pipeline scripts.');
        lines.push('');
    }

    lines.push('## Import Template');
    lines.push('');
    lines.push(`Timestamp: ${timestamp}`);
    lines.push('');
    lines.push('### Summary');
    lines.push('');
    lines.push(`- import file: \`${summary.inputCsv}\``);
    lines.push(`- valid rows: ${summary.validRows}`);
    lines.push(`- updated plants: ${summary.updatedPlants}`);
    lines.push(`- updated taxa: ${summary.updatedTaxa}`);
    lines.push('');
    lines.push('### Interpretation');
    lines.push('');
    if (summary.updatedPlants === 0 && summary.updatedTaxa === 0) {
        lines.push('- No database rows changed in this run. This usually means the approved names were absent from the connected database, or the target rows already had values outside the script update condition.');
    } else {
        lines.push('- Approved Chinese names were written into database preparation tables.');
    }
    lines.push('');

    fs.appendFileSync(reportPath, lines.join('\n'), 'utf8');
}

async function main() {
    if (!fs.existsSync(INPUT_CSV)) {
        throw new Error(`Import file not found: ${INPUT_CSV}`);
    }

    const content = fs.readFileSync(INPUT_CSV, 'utf8');
    const lines = content.split(/\r?\n/).filter(Boolean);
    if (lines.length <= 1) {
        console.log('CSV has no importable data.');
        return;
    }

    const header = parseCsvLine(lines[0]);
    const sciIdx = header.indexOf('scientific_name');
    const cnIdx = header.indexOf('chinese_name');
    const srcIdx = header.indexOf('source');

    if (sciIdx < 0 || cnIdx < 0) {
        throw new Error('CSV must contain scientific_name and chinese_name columns.');
    }

    const rows = [];
    for (let i = 1; i < lines.length; i += 1) {
        const cols = parseCsvLine(lines[i]);
        const scientificName = (cols[sciIdx] || '').trim();
        const chineseName = (cols[cnIdx] || '').trim();
        const source = (srcIdx >= 0 ? cols[srcIdx] : 'MANUAL').trim() || 'MANUAL';
        if (!scientificName || !hasChinese(chineseName)) continue;
        rows.push({ scientificName, chineseName, source });
    }

    if (rows.length === 0) {
        console.log('CSV contains no Chinese names to import.');
        return;
    }

    const conn = await mysql.createConnection(dbConfig);
    try {
        let updatedPlants = 0;
        let updatedTaxa = 0;

        for (const row of rows) {
            const [pResult] = await conn.query(
                `
                UPDATE plants
                SET chinese_name = ?
                WHERE scientific_name = ?
                  AND (chinese_name IS NULL OR chinese_name = '' OR chinese_name = scientific_name)
                `,
                [row.chineseName, row.scientificName]
            );
            updatedPlants += Number(pResult.affectedRows || 0);

            const [tResult] = await conn.query(
                `
                UPDATE taxa
                SET chinese_name = ?
                WHERE scientific_name = ?
                  AND taxon_rank = 'species'
                  AND (chinese_name IS NULL OR chinese_name = '' OR chinese_name = scientific_name)
                `,
                [row.chineseName, row.scientificName]
            );
            updatedTaxa += Number(tResult.affectedRows || 0);
        }

        appendImportReport({
            inputCsv: INPUT_CSV,
            validRows: rows.length,
            updatedPlants,
            updatedTaxa
        });

        console.log('Import completed:');
        console.log(`- file: ${INPUT_CSV}`);
        console.log(`- valid rows: ${rows.length}`);
        console.log(`- updated plants: ${updatedPlants}`);
        console.log(`- updated taxa: ${updatedTaxa}`);
    } finally {
        await conn.end();
    }
}

if (require.main === module) {
    main().catch((err) => {
        console.error(`Import template failed: ${err.message}`);
        process.exitCode = 1;
    });
}
